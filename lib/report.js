import github from '@actions/github'

export default function (data) {
  // max body size accepted in a comment is 65536 characters, see https://docs.github.com/rest/reference/issues#create-an-issue-comment
  const commentMaximumLength = 65536

  // extract relevant variables
  const { diff: { summary, patches }, textContent, customHeader, customFooter, showHeader, showFooter, showPlan, showDiff } = data
  const { runId, repo, sha } = github.context

  const warning = [
    '> [!WARNING]',
    `> The generated plan is too long, check [the output](https://github.com/${repo.owner}/${repo.repo}/actions/runs/${runId}) for the full detail.`
  ]

  // set a unique identifier for the comment to be able to remove it later if needed
  data.uniqueIdentifier = `<!-- ahmadnassri-action-terraform-report -->`
  data.runSignature = `<!-- ${repo.owner}-${repo.repo}-${sha}-${runId} -->`

  /* c8 ignore next 5 */
  const headerText = customHeader || [`### :robot: *Terraform Report*`, `---`]
  const footerText = customFooter || [`---`, `> [!NOTE]`, `> Generated by [Terraform Pull Request Report Generator](https://github.com/ahmadnassri/action-terraform-report) - run [\`#${runId}\`](https://github.com/${repo.owner}/${repo.repo}/actions/runs/${runId}) - commit ${sha}`]

  const header = [data.uniqueIdentifier, showHeader ? headerText : '']
  const footer = [showFooter ? footerText : '', data.runSignature]

  const details = `##### Plan: \`${summary.create}\` to add, \`${summary.update}\` to change, \`${summary.delete}\` to destroy`

  const content = []

  if (showPlan) {
    content.push('<details><summary>Show Plan</summary>', '')
    content.push('```terraform', textContent, '```')
    content.push('</details>', '')
  }

  if (showDiff) {
    const diff = patches.map(patch => ['```diff', patch, '```'])

    content.push(`<details><summary>Show Diff</summary>`, '')
    content.push(diff, '</details>')
  }

  // join body array into a string
  data.body = [header, '', details, '', content, '', footer].flat(3).join('\n')

  // show warning and link to the full output in the actions logs if the comment exceeds the maximum length
  if (data.body.length > commentMaximumLength) {
    data.body = [header, '', details, '', warning, '', footer].flat(3).join('\n')
  }
}
